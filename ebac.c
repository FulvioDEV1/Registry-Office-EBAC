#include <string.h>//Biblioteca de string(conjunto de variáveis)

int registro()// função responsável por cadastrar os usuários no sistema
{
	    setlocale(LC_ALL, "Portuguese");//Definindo a linguaguem
	
	char arquivo[40];//início da criação das variáveis
    char cpf[40];
    char nome[40];
    char sobrenome[40];
    char cargo[40];//fim de criação das variáveis
        
        printf("Digite o cpf a ser cadastrado: ");
		scanf("%s", cpf);  
		
		strcpy(arquivo, cpf);//responsavel pro copiar valores das strings
		
		FILE *file;//cria o arquivo
		file = fopen(arquivo, "w");//abre o arquivo
		fprintf(file, cpf);//salva a informação no arquivo
		fclose(file);//fecha o arquivo
		
		file = fopen(arquivo, "a");
		fprintf(file, ",");
		fclose(file);
		
		printf("Digite o nome a ser cadastrado: ");
		scanf("%s", nome);
		
		file = fopen(arquivo, "a");
		fprintf(file, nome);
		fclose(file);
		
		file = fopen(arquivo, "a");
		fprintf(file, ",");
		fclose(file);
		
		printf("Digite o sobrenome a ser cadastrado:");
		scanf("%s", sobrenome);
		
		file = fopen(arquivo, "a");
		fprintf(file, sobrenome);
		fclose(file);
		
		file = fopen(arquivo, "a");
		fprintf(file, ",");
		fclose(file);
		
		printf("Digite o cargo a ser cadastrado: ");
		scanf("%s", cargo);
		
		file = fopen(arquivo, "a");
		fprintf(file, cargo);
		fclose(file);
		
		file = fopen(arquivo, "a");
		fprintf(file, ".");
		fclose(file);
		
		system("pause");//pause a tela após as ações
	
		
}

int consulta()//função responsável por consultar os usuários do sistema
{
        setlocale(LC_ALL, "Portuguese");//definindo linguaguem
   
    char cpf[40];//criando variáveis
    char conteudo[200];
            
        printf("Digite o CPF a ser consultado: ");
        scanf("%s", cpf);
    
        FILE *file;//abrindo arquivo
        file = fopen(cpf, "r");
        
    if(file == NULL)
    {
        printf("não foi possível abrir o arquivo, não localizado!. \n");
	}
	
    while(fgets(conteudo, 200, file) != NULL)
	{
	    printf("\n Essas são as informações do usuário: ");
		printf("%s", conteudo);
		printf("\n\n");	
	}	
    
	    system("pause");
	    fclose(file);//fechando arquivo
	    
	          
}

